Diabetes mellitus, DM  (bahasa Latin: mellitus, rasa manis) yang juga dikenal di Indonesia dengan istilah penyakit kencing manis adalah kelainan metabolik yang disebabkan oleh banyak faktor, dengan simtoma berupa hiperglikemia kronis dan gangguan metabolisme karbohidrat, lemak dan protein, sebagai akibat dari:
			<ul>
    		<li>defisiensi sekresi hormon insulin, aktivitas insulin, atau keduanya.</li>
    		<li>defisiensi transporter glukosa.</li>
    		<li>atau keduanya.</li>
			</ul>

            <p>(Sumber: Wikipedia)            </p>
<p>Diabetes melitus atau kencing manis adalah penyakit akibat gangguan metabolisme karbohidrat yang disebabkan oleh jumlah insulin yang kurang atau karena kerja insulin yangtidak optimal, sehingga insulin tidak bisa masuk ke dalam sel dan hanya menumpuk di pembuluh darah. </p>
<p>Diabetes Melitus adalah penyakit yang ditandai oleh ketidak mampuan tubuh untuk memasukkan glukosa dari darah ke sel. Dengan demikian sel kekurangan glukosa, sedangkan darah mengandung glukosa berlebihan. 
  Glukosa darah yang sangat tinggi dapat mengakibatkan koma bahkan sampai 
  mati.</p>
